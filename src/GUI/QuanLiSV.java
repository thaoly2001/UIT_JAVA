    boolean KTTT() {
        String mssv = txtMaSV.getText();
        if (mssv.length() == 0) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên không được bỏ trống");
            txtMaSV.requestFocus();
            return false;
        }

        String tenNV = txtHoTen.getText();
        if (tenNV.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Họ và tên không được bỏ trống");
            txtHoTen.requestFocus();
            return false;
        }

        String mail = txtEmail.getText();
        String ddmail = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        if (mail.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Mail không được bỏ trống");
            txtEmail.requestFocus();
            return false;
        }
        if (mail.matches(ddmail) == false) {
            JOptionPane.showMessageDialog(this, "Mail Sai định dạng");
            return false;
        }
        String sdt = txtSoDT.getText();
        if (sdt.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Sdt không được bỏ trống");
            txtSoDT.requestFocus();
            return false;
        }
        
        String diachi = txaDiaChi.getText();
        if (diachi.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được bỏ trống");
            txaDiaChi.requestFocus();
            return false;
        }
        return true;
    }

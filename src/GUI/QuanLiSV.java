    private void chooseAvatar() {
        JFileChooser jfc = new JFileChooser();
        FileInputStream fis = null;
        FileOutputStream fos = null;
        int choice = jfc.showOpenDialog(this);
        if (choice == JFileChooser.APPROVE_OPTION) {
            try {
                String link = jfc.getSelectedFile().toString();
                String fileName = jfc.getSelectedFile().getName();
                String fileTypeName = fileName.substring(fileName.lastIndexOf("."), fileName.length()); // ps01.jpg/ tenFile.png
                fileSaveName = txtMaSV.getText() + fileTypeName; // lay masv lam ten file hinh
                fis = new FileInputStream(link);

                String saveLink = Paths.get("").toAbsolutePath().toString() + "/src/Image/" + fileSaveName; // src 
                fos = new FileOutputStream(saveLink);

                byte buffer[] = new byte[1024];
                int len = 0;
                while ((len = fis.read(buffer)) > 0) {
                    fos.write(buffer, 0, len);
                }

                BufferedImage avatar = ImageIO.read(new File(saveLink));
                lblHinh.setIcon(new ImageIcon(new ImageIcon(avatar).getImage().getScaledInstance(lblHinh.getWidth(), lblHinh.getHeight(), Image.SCALE_SMOOTH)));
                // hai gia tri 175, 200 chinh la 2 gia tri chieu rong + chieu cao cua label chua avatar

                fos.close();
                fis.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.toString());
                try {
                    if (fos != null) {
                        fos.close();
                    }
                    if (fis != null) {
                        fis.close();
                    }
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, "Error: " + ex.toString());
                }
            }
        }
    }

